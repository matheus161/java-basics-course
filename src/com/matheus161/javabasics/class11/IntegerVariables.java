package com.matheus161.javabasics.class11;

public class IntegerVariables {
    public static void main (String[] args) {
        byte age01 = 20;
        short age02 = 21;
        int age03 = 22;
        long age04 = 23;

        System.out.println("Variable age01 = " + age01);
        System.out.println("Variable age02 = " + age02);
        System.out.println("Variable age03 = " + age03);
        System.out.println("Variable age04 = " + age04);
    }
}
